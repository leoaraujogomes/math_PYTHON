# visualizar equivalências de ângulos em graus
import math

a = math.radians(30)
b = math.radians(45)
c = math.radians(60)

print("30º são ", a, "radianos")
print("45º são ", b, "radianos")
print("60º são ", c, "radianos")
print("90º são ", math.radians(90), "radianos")
print("180º são ", math.radians(180), "radianos")
